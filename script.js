var getPieceValue = function (piece, x, y) {
    if (piece === null) {
        return 0;
    }

    // Define the base values for each piece type
    const baseValues = {
        'p': 10,
        'r': 50,
        'n': 30,
        'b': 30,
        'q': 90,
        'k': 900
    };

    // Define piece-specific evaluation tables
    const evalTables = {
        'p': piece.color === 'w' ? pawnEvalWhite : pawnEvalBlack,
        'r': piece.color === 'w' ? rookEvalWhite : rookEvalBlack,
        'n': knightEval,
        'b': piece.color === 'w' ? bishopEvalWhite : bishopEvalBlack,
        'q': evalQueen,
        'k': piece.color === 'w' ? kingEvalWhite : kingEvalBlack
    };

    const { type, color } = piece;
    const baseValue = baseValues[type];
    const evalTable = evalTables[type];

    if (baseValue === undefined || evalTable === undefined) {
        throw "Unknown piece type: " + type;
    }

    // Calculate the absolute value of the piece
    const absoluteValue = baseValue + evalTable[y][x];
    return color === 'w' ? absoluteValue : -absoluteValue;
};

/* Board Visualisation and Game State Handling */
var onDragStart = function (piece) {
    if (game.in_checkmate() === true || game.in_draw() === true ||
        piece.search(/^b/) !== -1) {
        return false;
    }
};

var makeBestMove = function () {
    var bestMove = getBestMove(game);
    game.ugly_move(bestMove);
    board.position(game.fen());
    renderMoveHistory(game.history());
    if (game.game_over()) {
        alert('Game over');
    }
};


var positionCount;
var getBestMove = function (game) {
    if (game.game_over()) {
        alert('Game over');
    }

    positionCount = 0;
    var depth = parseInt($('#search-depth').find(':selected').text());

    var d = new Date().getTime();
    var bestMove = moveToPlay(depth, game, true);
    var d2 = new Date().getTime();
    var moveTime = (d2 - d);
    var positionsPerS = ( positionCount * 1000 / moveTime);

    $('#position-count').text(positionCount);
    $('#time').text(moveTime/1000 + 's');
    $('#positions-per-s').text(positionsPerS);
    return bestMove;
};

var renderMoveHistory = function (moves) {
    var historyElement = $('#move-history').empty();
    historyElement.empty();
    for (var i = 0; i < moves.length; i = i + 2) {
        historyElement.append('<span>' + moves[i] + ' ' + ( moves[i + 1] ? moves[i + 1] : ' ') + '</span><br>')
    }
    historyElement.scrollTop(historyElement[0].scrollHeight);

};

var onDrop = function (source, target) {

    var move = game.move({
        from: source,
        to: target,
        promotion: 'q'
    });

    removeGreySquares();
    if (move === null) {
        return 'snapback';
    }

    renderMoveHistory(game.history());
    window.setTimeout(makeBestMove, 250);
};

var onSnapEnd = function () {
    board.position(game.fen());
};

var onMouseoverSquare = function(square, piece) {
    var moves = game.moves({
        square: square,
        verbose: true
    });

    if (moves.length === 0) return;

    greySquare(square);

    for (var i = 0; i < moves.length; i++) {
        greySquare(moves[i].to);
    }
};

var onMouseoutSquare = function(square, piece) {
    removeGreySquares();
};

var removeGreySquares = function() {
    $('#board .square-55d63').css('background', '');
};

var greySquare = function(square) {
    var squareEl = $('#board .square-' + square);

    var background = '#a9a9a9';
    if (squareEl.hasClass('black-3c85d') === true) {
        background = '#696969';
    }

    squareEl.css('background', background);
};

